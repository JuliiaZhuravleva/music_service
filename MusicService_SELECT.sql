-- название и год выхода альбомов, вышедших в 2018 году;
SELECT  album_name, album_year 
FROM    album a 
WHERE   album_year = 2018;

-- название и продолжительность самого длительного трека;
SELECT      track_name, duration  
FROM        track t 
ORDER BY    duration    DESC 
LIMIT       1;

-- название и продолжительность самого длительного трека (второй вариант);
SELECT  track_name, duration 
FROM    track t 
WHERE   duration = (SELECT MAX(duration) FROM track t2);

-- название треков, продолжительность которых не менее 3,5 минуты;
SELECT  track_name 
FROM    track t 
WHERE   duration >= 210;

-- названия сборников, вышедших в период с 2018 по 2020 год включительно;
SELECT  compilation_name
FROM    compilation c 
WHERE   compilation_year    BETWEEN 2018 AND 2020;

-- исполнители, чье имя состоит из 1 слова;
SELECT  artist_name 
FROM    artist a 
WHERE   artist_name    NOT LIKE '% %';

-- название треков, которые содержат слово "мой"/"my".
SELECT  track_name 
FROM    track t 
WHERE   track_name          iLIKE '% my %' 
OR      lower(track_name)   LIKE '% мой %';